<p>BPEL has become the de-facto standard language for composing Web services. However, as every other programming language developers can introduce defects into the code that can lead to undesired consequences when the software is rolled out at the client-side. Therefore, all software development methodologies include testing as one of the important activities. For nearly every programming language, a unit testing framework exists, for example JUnit for Java. These testing frameworks allow developers to easily test small chunks of the code that they have written. Until recently, BPEL lacked such support. However, BPELUnit was developed for closing this gap.</p>

<p>You can freely download, distribute, and modify BPELUnit (as it is an Open Source project) and start testing your BPEL processes right away. On this site you will find the software and all necessary information for getting you started.</p>

